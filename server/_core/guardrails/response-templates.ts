/**
 * 获取随机响应
 */
export function getRandomResponse(category: ResponseCategory): string {
  const templates =
    RESPONSE_TEMPLATES[category] ?? RESPONSE_TEMPLATES["default"];
  const index = Math.floor(Math.random() * templates.length);
  return templates[index];
}

/**
 * 获取威胁等级对应的前缀标记（用于开发环境调试）
 */
export function getThreatPrefix(threatLevel: string): string {
  const prefixes: Record<string, string> = {
    low: "🟡",
    medium: "🟠",
    high: "🔴",
    critical: "🚫",
  };
  return prefixes[threatLevel] || "";
}
