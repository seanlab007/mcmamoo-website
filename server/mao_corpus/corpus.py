    def __init__(self, data_dir: str = None):
        """
        初始化向量库
        
        Args:
            data_dir: 数据目录，用于存储向量索引
        """
        self.data_dir = data_dir
        self.index = None
        self.embeddings = None
        self._build_index()
    
    def _build_index(self):
        """构建向量索引"""
        try:
            from sentence_transformers import SentenceTransformer
            
            # 使用轻量级模型
            self.model = SentenceTransformer('paraphrase-MiniLM-L3-v2')
            
            # 编码所有文档
            texts = [f"{doc['title']}。{doc['content']}" for doc in self.DOCUMENTS]
            self.embeddings = self.model.encode(texts)
            
            # 构建 FAISS 索引
            import faiss
            dimension = self.embeddings.shape[1]
            self.index = faiss.IndexFlatIP(dimension)  # 内积相似度
            
            # 归一化向量
            faiss.normalize_L2(self.embeddings)
            self.index.add(self.embeddings)
            
            print(f"✅ MaoCorpus 索引构建完成: {len(self.DOCUMENTS)} 篇文献")
            
        except ImportError:
            print("⚠️ sentence-transformers 或 faiss 未安装，使用关键词匹配模式")
            self.model = None
            self.index = None
    
    def query(self, query: str, top_k: int = 3) -> List[SearchResult]:
        """
        语义检索
        
        Args:
            query: 查询文本
            top_k: 返回结果数量
        
        Returns:
            搜索结果列表
        """
        if self.index is not None and self.model is not None:
            return self._semantic_search(query, top_k)
        else:
            return self._keyword_search(query, top_k)
    
    def _semantic_search(self, query: str, top_k: int) -> List[SearchResult]:
        """语义搜索"""
        import faiss
        
        # 编码查询
        query_embedding = self.model.encode([query])
        faiss.normalize_L2(query_embedding)
        
        # 搜索
        scores, indices = self.index.search(query_embedding, top_k)
        
        results = []
        for score, idx in zip(scores[0], indices[0]):
            if idx >= 0 and idx < len(self.DOCUMENTS):
                doc = self.DOCUMENTS[idx]
                results.append(SearchResult(
                    text=doc["content"],
                    source=doc["title"],
                    score=float(score),
                    metadata={
                        "keywords": doc["keywords"],
                        "year": doc["year"]
                    }
                ))
        
        return results
    
    def _keyword_search(self, query: str, top_k: int) -> List[SearchResult]:
        """关键词搜索（降级方案）"""
        query_words = set(query.lower().split())
        scores = []
        
        for doc in self.DOCUMENTS:
            text = f"{doc['title']} {doc['content']} {' '.join(doc['keywords'])}".lower()
            score = len(query_words & set(text.split()))
            scores.append((score, doc))
        
        # 排序取前k个
        scores.sort(key=lambda x: x[0], reverse=True)
        
        results = []
        for score, doc in scores[:top_k]:
            if score > 0:
                results.append(SearchResult(
                    text=doc["content"],
                    source=doc["title"],
                    score=float(score) / len(query_words) if query_words else 0,
                    metadata={
                        "keywords": doc["keywords"],
                        "year": doc["year"]
                    }
                ))
        
        return results
    
    def get_quote(self, topic: str) -> Optional[str]:
        """
        获取相关语录
        
        Args:
            topic: 主题
        
        Returns:
            语录文本
        """
        results = self.query(topic, top_k=1)
        if results:
            return f"【{results[0].source}】{results[0].text[:100]}..."
        return None
    
    def list_documents(self) -> List[Dict[str, Any]]:
        """列出所有文献"""
        return [
            {
                "title": doc["title"],
                "year": doc["year"],
                "keywords": doc["keywords"]
            }
            for doc in self.DOCUMENTS
        ]
