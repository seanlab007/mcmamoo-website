    def __init__(self):
        self.documents = self.DOCUMENTS

    def query(self, query: str, top_k: int = 3) -> List[DaoyanSearchResult]:
        """
        关键词检索（支持模糊匹配）

        Args:
            query: 查询文本
            top_k: 返回结果数量

        Returns:
            搜索结果列表
        """
        query_lower = query.lower()
        results = []

        for doc in self.documents:
            text = f"{doc['title']} {doc['content']} {' '.join(doc['keywords'])}".lower()

            # 模糊匹配：检查查询词是否出现在文档中
            if query_lower in text:
                # 计算匹配次数作为分数
                score = text.count(query_lower)

                results.append(DaoyanSearchResult(
                    title=doc['title'],
                    content=doc['content'],
                    source=f"{doc['source']} | {doc['date']}",
                    score=score,
                    metadata={
                        'author': doc['author'],
                        'keywords': doc['keywords'],
                        'year': doc['year']
                    }
                ))

        # 按分数排序
        results.sort(key=lambda x: x.score, reverse=True)
        return results[:top_k]

    def get_stats(self) -> Dict[str, Any]:
        """获取统计信息"""
        return {
            'total_documents': len(self.DOCUMENTS),
            'years': list(set(d['year'] for d in self.DOCUMENTS)),
            'categories': list(set(d['keywords'][0] for d in self.DOCUMENTS if d['keywords']))
        }

    def list_titles(self) -> List[Dict[str, str]]:
        """列出所有文章"""
        return [
            {
                'title': d['title'],
                'date': d['date'],
                'author': d['author']
            }
            for d in self.DOCUMENTS
        ]


